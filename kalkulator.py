import math
kom = "Dzielenie przez zero"

def dodawanie(liczba,liczba2):
    return liczba + liczba2
def odejmowanie(liczba,liczba2):
    return liczba - liczba2
def mnozenie(liczba,liczba2):
    return liczba * liczba2
def dzielenie(liczba,liczba2):
    return liczba / liczba2
def potega(liczba):
    return liczba**2
def pierwiastkowanie(liczba):
    return math.sqrt(liczba)
def procent(liczba,liczba2):
    return liczba % liczba2
def stopnieF(liczba):
    return 1.8 * liczba + 32
def stopnieK(liczba):
    return 273.15 * liczba